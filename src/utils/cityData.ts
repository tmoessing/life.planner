// Get all cities for a specific state
export const getCitiesForState = (state: string): string[] => {
  return usCitiesByState[state] || [];
};

// Get all cities across all states (for general search)
export const getAllCities = (): string[] => {
  return Object.values(usCitiesByState).flat();
};

// Search cities by name (case-insensitive)
export const searchCities = (searchTerm: string, state?: string): string[] => {
  const cities = state ? getCitiesForState(state) : getAllCities();
  return cities.filter(city => 
    city.toLowerCase().includes(searchTerm.toLowerCase())
  );
};
