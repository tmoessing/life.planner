// Get all regions for a specific country
export const getRegionsForCountry = (country: string): string[] => {
  return countriesWithRegions[country] || [];
};

// Get all countries
export const getAllCountries = (): string[] => {
  return Object.keys(countriesWithRegions);
};

// Search countries by name (case-insensitive)
export const searchCountries = (searchTerm: string): string[] => {
  return getAllCountries().filter(country => 
    country.toLowerCase().includes(searchTerm.toLowerCase())
  );
};

// Search regions by name within a country (case-insensitive)
export const searchRegions = (searchTerm: string, country: string): string[] => {
  const regions = getRegionsForCountry(country);
  return regions.filter(region => 
    region.toLowerCase().includes(searchTerm.toLowerCase())
  );
};
